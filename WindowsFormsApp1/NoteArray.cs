        /// <summary>
        /// 노트 종류별 동작 시행
        /// </summary>
        /// <param name="_cnt">배열 번호</param>
        /// <param name="_noteArray">해석할 노트 배열</param>
        private void testNodeSwitcher(int _cnt) {


            if ((noteArray[_cnt] >> 32) != 0)
            {
                switch ((int)(noteArray[_cnt] >> 32))
                {

                    case 1:

                        currentFocusScene = Scene.STAGE_2;
                        SceneChanger.ChangeScene(stage_1, stage_2, false, 10, 0.5f, true);
                        stage_1.testNodeSwitcher(_cnt, noteArray);

                        break;

                    case 2:
                        currentFocusScene = Scene.STAGE_3;
                        SceneChanger.ChangeScene(stage_1, stage_3, false, 10, 0.5f, true);
                        stage3Subform.Show();
                        stage3Subform.passFocus();
                        stage_1.testNodeSwitcher(_cnt, noteArray);

                        break;
                    case 3:

                        currentFocusScene = Scene.STAGE_1;
                        SceneChanger.ChangeScene(stage_2, stage_1, false, 10, 0.5f, true);
                        stage_2.testNodeSwitcher(_cnt, noteArray, stage2_sectionArray);

                        break;
                    case 4:

                        currentFocusScene = Scene.STAGE_3;
                        SceneChanger.ChangeScene(stage_2, stage_3, false, 10, 0.5f, true);
                        stage3Subform.Show();
                        stage3Subform.passFocus();
                        stage_2.testNodeSwitcher(_cnt, noteArray, stage2_sectionArray);


                        break;
                    case 5:

                        currentFocusScene = Scene.STAGE_1;
                        SceneChanger.ChangeScene(stage_3, stage_1, false, 10, 0.5f, true);
                        stage3Subform.Visible = false;
                        SceneChanger.moveUp();
                        stage_3.testNodeSwitcher(_cnt, noteArray);


                        break;
                    case 6:

                        currentFocusScene = Scene.STAGE_2;
                        SceneChanger.ChangeScene(stage_3, stage_2, false, 10, 0.5f, true);
                        stage3Subform.Visible = false;
                        SceneChanger.moveUp();
                        stage_3.testNodeSwitcher(_cnt, noteArray);

                        break;

                    case 7:
                        stage3Subform.Visible = false;
                        SceneChanger.ChangeScene(stage_3, Scene.MAIN_MENU);
                        stage_1.init();
                        stage_2.init();
                        stage_3.init();
                        init();
                        
                        break;

                    case 8://점수정산 및 넘어가기
                        score = stage_1.getScore() * 3 + stage_2.getScore() * 5 + stage_3.getScore();
                        int clearLevel = score >= 257 ? 0 : score >= 110 ? 1 : 2;
                        SceneChanger.SetResultScene(Scene.STAGE_4, clearLevel);
                        stage3Subform.Visible = false;
                        SceneChanger.ChangeScene(stage_3, Scene.RESULT);
                        stage_1.init();
                        stage_2.init();
                        stage_3.init();
                        init();
                        break;

                    default:
                        break;
                }
            }
            else {

                switch (currentFocusScene) {

                    case Scene.STAGE_1:
                        stage_1.testNodeSwitcher(_cnt, noteArray);
                        break;
                    case Scene.STAGE_2:
                        stage_2.testNodeSwitcher(_cnt, noteArray, stage2_sectionArray);
                        break;
                    case Scene.STAGE_3:
                        stage_3.testNodeSwitcher(_cnt, noteArray);
                        break;
                    default:
                        break;


                }

            }

        }
         
