        /// <summary>
        /// 노트 해석
        /// </summary>
        /// <param name="value">값</param>
        /// <param name="mask">마스트</param>
        /// <param name="shift">시프트</param>
        /// <returns></returns>
        internal int ToIndex(ulong value, ulong mask, int shift)
        {
            return (int)((value & mask) >> shift) - 1;
        }

        /// <summary>
        /// 노트 종류별 동작 시행
        /// </summary>
        /// <param name="_cnt">배열 번호</param>
        /// <param name="_noteArray">해석할 노트 배열</param>
        internal void testNodeSwitcher(int _cnt, ulong[] _noteArray)
        {
            subForm.updateDump();

            if (_cnt < _noteArray.Length && _cnt >= 0)
            {

                if ((_noteArray[_cnt] & CURSOR_IMAGE_MASK) != 0)
                {
                    arrowPictureBox.Image = arrowImages[ToIndex(_noteArray[_cnt],CURSOR_IMAGE_MASK, CURSOR_IMAGE_SHIFT)];
                }

                if ((_noteArray[_cnt] & SMOKE_IMAGE_MASK) != 0)
                {
                    smokePictureBox.Image = smokeImages[ToIndex(_noteArray[_cnt], SMOKE_IMAGE_MASK, SMOKE_IMAGE_SHIFT)];
                }

                if ((_noteArray[_cnt] & BLINK_MASK) != 0)
                {
                    int type = ToIndex(_noteArray[_cnt], BLINK_MASK, BLINK_SHIFT);

                    if (type < 8)
                    {
                        SceneChanger.Blink(commandPictureBox, commandImages[type], 430);
                    }
                    else if (type < 11)
                    {
                        SceneChanger.Blink(smokePictureBox, smokeImages[type - 8], 430);
                    }
                    else
                    {
                        SceneChanger.Blink(smokePictureBox, smokeImages[2], smokeImages[0], 60);
                    }

                }

                if ((_noteArray[_cnt] & CUR_SND_MASK) != 0)
                {
                    SoundPlayer.PlayFX(MediaType.CUR_MOVE);//0.8
                }

                if ((_noteArray[_cnt] & CMD_SND_MASK) != 0)
                {
                    SoundPlayer.PlayFX(MediaType.STAGE1_DOUBLESHOT);
                }

                if ((_noteArray[_cnt] & STEAM_SND_MASK) != 0)
                {
                    SoundPlayer.PlayFX(MediaType.STEAM);//0.4
                }

                if ((_noteArray[_cnt] & MACH_SND_MASK) != 0)
                {
                    SoundPlayer.PlayFX(MediaType.MACHIENE);//0.3
                }

                if ((_noteArray[_cnt] & SCENE_MOVE_MASK) != 0)
                {
                    if (ToIndex(_noteArray[_cnt], SCENE_MOVE_MASK, SCENE_MOVE_SHIFT) == 0)
                    {
                        SceneChanger.moveUp();
                    }
                    else {
                        SceneChanger.moveDown();
                    }
                }

                if ((_noteArray[_cnt] & SHAKE_TIMER_MASK) != 0)
                {
                    if (ToIndex(_noteArray[_cnt], SHAKE_TIMER_MASK, SHAKE_TIMER_SHIFT) == 0)
                    {
                        Timers.StartTimer(TimerType.SCREEN_SHAKE_TIMER);
                    }
                    else {
                        Timers.StopTimer(TimerType.SCREEN_SHAKE_TIMER);
                    }
                }

                if ((_noteArray[_cnt] & HELP_TIMER_MASK) != 0)
                {
                    if (ToIndex(_noteArray[_cnt], HELP_TIMER_MASK, HELP_TIMER_SHIFT) == 0)
                    {
                        Timers.StartTimer(TimerType.STAGE3_HELPER_TIMER);
                    }
                    else
                    {
                        Timers.StopTimer(TimerType.STAGE3_HELPER_TIMER);
                    }
                }

                if ((_noteArray[_cnt] & STAGE_TIMER_MASK) != 0)
                {
                    if (ToIndex(_noteArray[_cnt], STAGE_TIMER_MASK, STAGE_TIMER_SHIFT) == 0)
                    {
                        Timers.StartTimer(TimerType.STAGE_TIC_TIMER);
                    }
                    else
                    {
                        Timers.StopTimer(TimerType.STAGE_TIC_TIMER);
                    }
                }

                if ((_noteArray[_cnt] & TEXT_TIMER_MASK) != 0)
                {
                    if (ToIndex(_noteArray[_cnt], TEXT_TIMER_MASK, TEXT_TIMER_SHIFT) == 0)
                    {
                        Timers.StartTimer(TimerType.TEXT_TIMER);
                    }
                    else
                    {
                        Timers.StopTimer(TimerType.TEXT_TIMER);
                    }
                }

                if ((_noteArray[_cnt] & RESET_POS_MASK) != 0) {
                    subForm.resetPos();
                }

                if ((_noteArray[_cnt] & ISCURRECT_MASK) != 0) {

                    switch (ToIndex(_noteArray[_cnt], ISCURRECT_MASK, ISCURRECT_SHIFT)) {

                        case 0:
                            curLeft = true;
                            curRight = false;
                            curDown = false;
                            break;

                        case 1:
                            curLeft = false;
                            curRight = true;
                            curDown = false;
                            break;

                        case 2:
                            curLeft = false;
                            curRight = false;
                            curDown = true;
                            break;

                        case 3:
                            curLeft = false;
                            curRight = false;
                            curDown = false;
                            break;

                        case 4:
                            if (Timers.StopTimer(TimerType.SCREEN_SHAKE_TIMER)) SceneChanger.moveUp();
                            curLeft = false;
                            curRight = false;
                            curDown = false;
                            break;

                        default:
                            break;
                    }

                }

                if ((_noteArray[_cnt] & ALLOW_INPUT_MASK) != 0)
                {
                    stageMode = StageMode.NOMAL;
                }

                if ((_noteArray[_cnt] & TUTO_SECTION_CHECK_TYPE_MASK) != 0)
                {
                    switch (ToIndex(_noteArray[_cnt], TUTO_SECTION_CHECK_TYPE_MASK, TUTO_SECTION_CHECK_TYPE_SHIFT)) {

                        case 0:
                            bgm.Pause();
                            TextboxVisible(true);
                            break;

                        case 1:
                            stageMode = StageMode.AUTO;

                            TutorialSectionCheck(8, 64);
                            /*
                            if (score == 8)
                            {
                                textListCounter += 2;
                            }
                            else
                            {
                                cnt -= 64;
                                cntex = cntex - (bpm * 64);
                                bgm.Position = new TimeSpan(0, 0, 0, 0, (int)(bgm.Position.TotalMilliseconds - bpm * 64));

                            }
                            
                            score = 0;
                            TextboxVisible(true);
                            bgm.Pause();
                            */
                            break;

                        case 2:
                            stageMode = StageMode.AUTO;

                            if (TutorialSectionCheck(19, 96)) {
                                subForm.Visible = true;
                                subForm.passFocus();
                            }
                            /*
                            if (score == 19)
                            {
                                textListCounter += 2;
                                subForm.Visible = true;
                                subForm.passFocus();
                            }
                            else
                            {
                                cnt -= 96;
                                cntex = cntex - (bpm * 96);
                                bgm.Position = new TimeSpan(0, 0, 0, 0, (int)(bgm.Position.TotalMilliseconds - bpm * 96));

                            }

                            score = 0;
                            TextboxVisible(true);
                            bgm.Pause();
                            */
                            break;

                        case 3:
                            stageMode = StageMode.AUTO;

                            subForm.Visible = !TutorialSectionCheck(18, 96);
                            /*
                            if (score == 18)
                            {
                                subForm.Visible = false;
                                textListCounter += 2;
                            }
                            else
                            {
                                cnt -= 96;
                                cntex = cntex - (bpm * 96);
                                bgm.Position = new TimeSpan(0, 0, 0, 0, (int)(bgm.Position.TotalMilliseconds - bpm * 96));

                            }

                            score = 0;
                            TextboxVisible(true);
                            bgm.Pause();*/
                            break;

                        case 4:
                            subForm.Visible = false;
                            SceneChanger.ChangeScene(Scene.MAIN_MENU);
                            init();
                            break;

                        case 5:
                            subForm.Visible = false;
                            int clearLevel = score >= 175 ? 0 : score >= 70 ? 1 : 2;
                            SceneChanger.SetResultScene(Scene.STAGE_3, clearLevel);
                            SceneChanger.ChangeScene(Scene.RESULT);
                            init();
                            break;

                        default:
                            break;

                            
                    }
                }

            }


        }


