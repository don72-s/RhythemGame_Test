        /// <summary>
        /// 노트 종류별 동작 시행
        /// </summary>
        /// <param name="_cnt">배열 번호</param>
        /// <param name="_noteArray">해석할 노트 배열</param>
        internal void testNodeSwitcher(int _cnt, ulong[] _noteArray, int[] _sectionArray) {

            if (_cnt >= 0 && _cnt < _noteArray.Length) {

                ulong tmp = _noteArray[_cnt];

                if ((tmp & IMG_MASK) != 0) {
                    characterPicturebox.Image = characterImgList[((int)(tmp & IMG_MASK)) - 1];
                }

                if ((tmp & RAINDROP1_BLINK_MASK) != 0)
                {
                    SceneChanger.Blink(r1, r1BlinkImg, 54);
                }

                if ((tmp & RAINDROP2_BLINK_MASK) != 0)
                {
                    SceneChanger.Blink(r2, r2BlinkImg, 54);
                }

                if ((tmp & RAINDROP1_SND_MASK) != 0)
                {
                    SoundPlayer.PlayFX(MediaType.RAINDROP_1);
                }

                if ((tmp & RAINDROP2_SND_MASK) != 0)
                {
                    SoundPlayer.PlayFX(MediaType.RAINDROP_2);
                }

                if ((tmp & CMD_SND_MASK) != 0)
                {
                    SoundPlayer.PlayFX(MediaType.STAGE1_DOUBLESHOT);
                }

                if ((tmp & MISTAKE_SND_MASK) != 0)
                {
                    SoundPlayer.PlayFX(MediaType.MISTAKE);
                }

                if ((tmp & ISCURRECT_MASK) != 0)
                {
                    switch ((int)((tmp & ISCURRECT_MASK) >> ISCURRECT_SHIFT)) {

                        case 1:
                            isCurrectLeft = true;
                            isCurrectRight = false;
                            break;
                        case 2:
                            isCurrectLeft = false;
                            isCurrectRight = true;
                            break;
                        case 3:
                            isCurrectLeft = false;
                            isCurrectRight = false;
                            break;
                        default:
                            break;
                    }
                }

                if ((tmp & INPUT_NOMAL_MASK) != 0)
                {
                    stageMode = StageMode.NOMAL;
                }

                if ((tmp & INPUT_AUTO_MASK) != 0)
                {
                    stageMode = StageMode.AUTO;
                }

                if ((tmp & SETTING_MASK) != 0)
                {
                    sectionCmpPoint = _sectionArray[sectArrayCnt];
                    sectArrayCnt++;
                    sectionPoint = 0;
                }

                if ((tmp & CHECK_MASK) != 0)
                {
                    if (sectionCmpPoint == sectionPoint)
                    {
                        SoundPlayer.PlayFX(MediaType.STAGE1_DOUBLESHOT);
                        characterPicturebox.Image = characterImgList[11];
                        addScore(1);
                    }
                    else
                    {
                        SoundPlayer.PlayFX(MediaType.MISTAKE);
                        characterPicturebox.Image = characterImgList[12];
                    }
                    sectionPoint = 0;
                }

                if ((tmp & TUTO_CHECK_MASK) != 0) {

                    switch ((int)((tmp & TUTO_CHECK_MASK) >> TUTO_CHECK_SHIFT)) {

                        case 1:
                            score = 0;
                            TextboxVisible(true);
                            bgm.Pause();
                            Timers.StopTimer(TimerType.STAGE_TIC_TIMER);
                            Timers.StartTimer(TimerType.TEXT_TIMER);
                            break;

                        case 2:

                            if (TutorialSectionCheck(2, 192))
                            {
                                stageMode = StageMode.AUTO;
                            }
                            else {
                                sectArrayCnt -= 2;
                            }
                            Timers.StopTimer(TimerType.STAGE_TIC_TIMER);
                            Timers.StartTimer(TimerType.TEXT_TIMER);

                            break;


                    }

                }

                if ((tmp & ENDTYPE_MASK) != 0) {
                    if ((int)((tmp & ENDTYPE_MASK) >> ENDTYPE_SHIFT) == 1)
                    {
                        SceneChanger.ChangeScene(Scene.MAIN_MENU);
                        init();
                    }
                    else {
                        int clearLevel = score >= 18 ? 0 : score >= 8 ? 1 : 2;
                        SceneChanger.SetResultScene(Scene.STAGE_2, clearLevel);
                        SceneChanger.ChangeScene(Scene.RESULT);
                        init();
                    }
                }


            }

        }



