        /// <summary>
        /// 노트 종류별 동작 시행
        /// </summary>
        /// <param name="_cnt">배열 번호</param>
        /// <param name="_noteArray">해석할 노트 배열</param>
        internal void testNodeSwitcher(int _cnt, ulong[] _noteArray)
        {

            if (_cnt < _noteArray.Length && _cnt >= 0)
            {

                ulong tmp = _noteArray[_cnt];

                if ((tmp & 0xfffff) == 0) {
                    isCurrect = false;
                    return;
                }

                if ((tmp & IMG_CHANGE_MASK) != 0) {
                    int index = (int)((tmp & IMG_CHANGE_MASK) >> IMG_CHANGE_SHIFT);

                    if (index == 1)
                    {
                        _p.pictureBox.Image = _p.pictureBox.Image.Equals(_p.images[0]) ? _p.images[1] : _p.images[0];
                    }
                    else if (index == 8)
                    {
                        _p.pictureBox.Image = _p.images[0];
                    }
                    else if (index == 9) {
                        _p.pictureBox.Image = _p.images[1];
                    }
                    else
                    {
                        _p.pictureBox.Image = _p.images[index];
                    }

                }            

                if ((tmp & CUR_SND_MASK) != 0) {
                    SoundPlayer.PlayFX(MediaType.CUR_MOVE);
                }

                if ((tmp & DOUBLE_SND_MASK) != 0)
                {
                    SoundPlayer.PlayFX(MediaType.STAGE1_DOUBLESHOT);
                }

                if ((tmp & CAMERA_SND_MASK) != 0)
                {
                    SoundPlayer.PlayFX(MediaType.CAMERA);
                }

                if ((tmp & BLINK_MASK) != 0) {
                    SceneChanger.Blink(blinkEffectPicturebox, cameraFlashBlinkImg, 70);
                }

                if ((tmp & IS_CURRENT_MASK) != 0) {
                    isCurrect = ((tmp & IS_CURRENT_MASK) >> IS_CURRENT_SHIFT == 1) ? true : false;
                }

                if ((tmp & STAGETIMER_STOP_MASK) != 0) {
                    Timers.StopTimer(TimerType.STAGE_TIC_TIMER);
                }

                if ((tmp & TEXTTIMER_START_MASK) != 0)
                {
                    Timers.StartTimer(TimerType.TEXT_TIMER);
                }

                if ((tmp & SECTION_MASK) != 0) {

                    switch ((ulong)((tmp & SECTION_MASK) >> SECTION_SHIFT)) {

                        case 1:

                            if (TutorialSectionCheck(2, 160)) stageMode = StageMode.AUTO;

                            break;
                        case 2:

                            if (TutorialSectionCheck(4, 160)) stageMode = StageMode.AUTO;

                            break;
                        case 3:

                            if (TutorialSectionCheck(6, 160)) stageMode = StageMode.AUTO;

                            break;
                        case 4:
                            stageMode = StageMode.TUTORIAL;
                            score = 0;

                            TextboxVisible(true);
                            bgm.Pause();
                            break;

                    }
                }

                if ((tmp & ENDTYPE_MASK) != 0) {
                    if ((int)((tmp & ENDTYPE_MASK) >> ENDTYPE_SHIFT) == 1)
                    {
                        SceneChanger.ChangeScene(Scene.MAIN_MENU);
                        init();
                    }
                    else {
                        Timers.StopTimer(TimerType.STAGE_TIC_TIMER);

                        int clearLevel = score >= 70 ? 0 : score >= 30 ? 1 : 2;
                        SceneChanger.SetResultScene(Scene.STAGE_1, clearLevel);
                        SceneChanger.ChangeScene(Scene.RESULT);
                        init();
                    }
                }

            }


        }

